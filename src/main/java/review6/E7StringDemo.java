package review6;

public class E7StringDemo {
    public static void main(String[] args) {

        System.out.println(Character.toUpperCase('A'));
        System.out.println(Character.toUpperCase('a'));//turns to uppercase
        System.out.println(Character.isLowerCase('D'));//false. D is not lower case
        System.out.println(Character.isDigit('2'));//true
        System.out.println(Character.isDigit('a'));//false
        System.out.println(Character.isAlphabetic('#'));//false
        System.out.println(Character.isAlphabetic('5'));//false
        System.out.println(Character.isDigit('r'));//false


       }
    }

