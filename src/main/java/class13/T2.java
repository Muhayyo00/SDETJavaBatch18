package class13;

public class T2 {
    /*Create a method that will take a number and prints
    whether the number is even or odd.
     */
    void oddOrEven(int num) {
        if(num%2==0) {
            System.out.println(num+" is even");
        }else{
            System.out.println(num+" is odd");
        }
    }
}
