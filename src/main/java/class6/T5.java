package class6;

public class T5 {
    public static void main(String[] args) {

    //Print odd numbers from 100 to 1(2 different way)

int b=99;//while loop
while(b>=1) {
    System.out.print(b+" ");
    b=b-2;
}


int f=99;//do while loop
do{
    System.out.print(f+" ");
    f=f-2;
}while(f>=1);


for(int m=99; m>=1; m=m-2) {//for loop
    System.out.println(m+" ");
}
    }
}
