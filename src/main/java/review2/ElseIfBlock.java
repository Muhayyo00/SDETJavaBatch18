package review2;

public class ElseIfBlock {
    public static void main(String[] args) {
        int WorldCup=2010;
        if (WorldCup==2002) {
            System.out.println("The host country was Korea");
        }else if(WorldCup==2006) {
            System.out.println("The host country was Germany");
        }else if(WorldCup==2010) {
            System.out.println("The host country was South Africa");
        }else if (WorldCup==2014) {
            System.out.println("The host country was Brazil");
        }else{
            System.out.println("The host country is unknown");
        }



    }
}
